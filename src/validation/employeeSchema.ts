import { z } from "zod";

export const employeeSchema = z.object({
  name: z
    .string()
    .min(2, "ФИО должно содержать минимум 2 символа")
    .max(100, "ФИО не должно превышать 100 символов")
    .regex(
      /^[а-яёА-ЯЁa-zA-Z\s-]+$/,
      "ФИО должно содержать только русские и английские буквы, пробелы и дефисы"
    ),

  manager: z
    .string()
    .min(2, "Имя руководителя должно содержать минимум 2 символа")
    .max(100, "Имя руководителя не должно превышать 100 символов")
    .regex(
      /^[а-яёА-ЯЁa-zA-Z\s-]+$/,
      "Имя руководителя должно содержать только русские и английские буквы, пробелы и дефисы"
    ),

  position: z.string().min(1, "Должность обязательна для заполнения"),
});

export type EmployeeFormData = z.infer<typeof employeeSchema>;
